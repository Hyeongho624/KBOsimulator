상대_타자_리스트 = get_상대_타자_정보()  # ex. ['홍길동', '이대호', '강백호']

def 상대타자_특징_벡터(타자들):
    벡터들 = []
    for 타자 in 타자들:
        특징 = [
            타자.vs_우투_AVG,
            타자.vs_좌투_OBP,
            타자.SLG,
            타자.wRC_plus
        ]
        벡터들.append(특징)
    return 평균벡터(벡터들)

def 불펜_투수_특징_벡터(투수):
    return [
        투수.vs_우타_피안타율,
        투수.vs_좌타_피출루율,
        투수.피장타율,
        투수.wOBA_허용
    ]

def 유클리드거리(v1, v2):
    return sqrt(sum((a - b)**2 for a, b in zip(v1, v2)))

# 상대 타자진의 특징 벡터
타자_벡터 = 상대타자_특징_벡터(상대_타자_리스트)

# 각 불펜 투수와 거리 계산
불펜_목록 = get_우리팀_불펜()
거리_목록 = []
for 투수 in 불펜_목록:
    투수_벡터 = 불펜_투수_특징_벡터(투수)
    거리 = 유클리드거리(타자_벡터, 투수_벡터)
    거리_목록.append((투수.이름, 거리))

# 거리 기준으로 정렬 후 top-K 선택
추천_불펜 = 정렬(거리_목록)[:K]
출력(추천_불펜)
